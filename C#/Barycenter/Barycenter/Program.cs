using System;

namespace Barycenter
{
    class Program
    {
        public static double[] BarTriang(
            double[] a,
            double[] b,
            double[] c)
        {
            var x = Math.Round(((a[0] + b[] + c[]) / 3), 4);
            var y = Math.Round(((a[1] + b[1] + c[1]) / 3), 4);

            return new[] { x, y };
        }
    }
}
